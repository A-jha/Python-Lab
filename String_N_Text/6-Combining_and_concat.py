

# 1. Join method
parts = ['Is', 'big', 'if', 'fat']
space_sperated_combined_parts = ' '.join(parts)
print(space_sperated_combined_parts)
# ---->Is big if fat


comma_saperated_join = ','.join(parts)
print(comma_saperated_join)
# --->Is,big,if,fat


# 2 . use '+' to conctinate
